
#include <iostream>
using namespace std;

int main()
{
	// Use the sizeof() function to return the size of integers, longs
	// and doubles

	// Define the variable aplies
	long double apple;

	cout << "The size of an integer is " << sizeof(int) << " bytes.\n";
	cout << "The size of a long integer is " << sizeof(long) << " bytes.\n";
	cout << "An apple can be eaten in " << sizeof(apple) << " bytes.\n";

	return 0;
}


